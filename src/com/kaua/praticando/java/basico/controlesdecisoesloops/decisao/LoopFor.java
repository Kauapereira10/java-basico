package com.kaua.praticando.java.basico.controlesdecisoesloops.decisao;

public class LoopFor {
	public static void main(String[] args) {
		
		// incremento
		for(int i=0; i <= 5; i++) {
			System.out.println("i tem valor: " + i);
		}
		
		System.out.println("-------------------");
		
		// decremento
		for (int i=5; i >= 0; i--) {
			System.out.println("i tem valor: " + i);
		}
		
		/*
		 * for (inicializacao; condicao; atualizacao) {
		 * 		bloco de codigo
		 *  }
		 * */
	}
}
