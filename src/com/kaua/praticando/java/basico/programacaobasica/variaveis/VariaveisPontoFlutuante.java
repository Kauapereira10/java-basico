package com.kaua.praticando.java.basico.programacaobasica.variaveis;

public class VariaveisPontoFlutuante {

    public static void main(String[] args) {

        /*
         * TIPOS DECIMAIS EM JAVA
         * double -> padrão para números decimais
         * float  -> precisa de F ou f no final
         */

        double valorPassagem = 2.90;
        float valorTomate = 3.53F;

        System.out.println("Valor da passagem: " + valorPassagem);
        System.out.println("Valor do tomate  : " + valorTomate);
    }
}