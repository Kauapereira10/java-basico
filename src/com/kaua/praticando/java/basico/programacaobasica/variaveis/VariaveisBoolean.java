package com.kaua.praticando.java.basico.programacaobasica.variaveis;

public class VariaveisBoolean {

    public static void main(String[] args) {

        /*
         * BOOLEAN
         * Só aceita dois valores:
         * true  ou false
         */

        boolean ativo = true;
        boolean bloqueado = false;

        System.out.println("Usuário ativo     : " + ativo);
        System.out.println("Usuário bloqueado : " + bloqueado);
    }
}